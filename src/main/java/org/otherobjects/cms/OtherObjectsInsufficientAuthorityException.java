package org.otherobjects.cms;

public class OtherObjectsInsufficientAuthorityException extends RuntimeException
{
    
    private static final long serialVersionUID = 3842548171374113183L;

    public OtherObjectsInsufficientAuthorityException()
    {
        super();    
    }

    public OtherObjectsInsufficientAuthorityException(String message)
    {
        super(message);
    }

    public OtherObjectsInsufficientAuthorityException(Throwable cause)
    {
        super(cause);
    }

    public OtherObjectsInsufficientAuthorityException(String message, Throwable cause)
    {
        super(message, cause);
    }

}
